
public class DefaultVal {
	
	private byte a;
	private short b;
	private int c;
	private long d;
	private float e;
	private double f;
	private boolean g;
	private char h;
	

	public static void main(String args[])
	{
		DefaultVal s = new DefaultVal();
		
		System.out.println("Default val : ");
		System.out.println("byte = "+s.a);
		System.out.println("short = "+s.b);
		System.out.println("int = "+s.c);
		System.out.println("long = "+s.d);
		System.out.println("flaot = "+s.e);
		System.out.println("double = "+s.f);
		System.out.println("boolean = "+s.g);
		System.out.println("char = "+s.h);

	}
}
